clc; close all; clear;
% ---------------------------
q1 = deg2rad(0);
q2 = deg2rad(0);
q3 = deg2rad(0);
q4 = deg2rad(0);
% syms q1 q2 q3
% assume([q1 q2 q3],'real')
% ---------------------------
r0_0 = [0;0;0];
R0_0 = eye(3);
r0_1 = [0;0;100];
r1_2 = [0;0;450];
r2_3 = [0;-90;0];
r3_4 = [400;0;0];
r4_5 = [0;90;0];
r5_6 = [0;0;-350];
r6_7 = [0;0;-170];
% ---------------------------
R0_1 = Rz(q1);
R1_2 = eye(3);
R0_2 = R0_1*R1_2;
R2_3 = Ry(q2);
R0_3 = R0_2*R2_3;
R3_4 = eye(3);
R0_4 = R0_3*R3_4;
R4_5 = Ry(q3);
R0_5 = R0_4*R4_5;
R5_6 = Rz(q4);
R0_6 = R0_5*R5_6;
R6_7 = eye(3);
R0_7 = R0_6*R6_7;
% ---------------------------
r0_2 = r0_1 + R0_1*r1_2;
r0_3 = r0_2 + R0_2*r2_3;
r0_4 = r0_3 + R0_3*r3_4;
r0_5 = r0_4 + R0_4*r4_5;
r0_6 = r0_5 + R0_5*r5_6;
r0_7 = r0_6 + R0_6*r6_7;
% ---------------------------
o0 = [R0_0 r0_0];
o1 = [R0_1 r0_1];
o2 = [R0_2 r0_2];
o3 = [R0_3 r0_3];
o4 = [R0_4 r0_4];
o5 = [R0_5 r0_5];
o6 = [R0_6 r0_6];
o7 = [R0_7 r0_7];
%%
figure
a = 50;
vlink(r0_0,r0_1,'o')
vlink(r0_1,r0_2,'o')
vlink(r0_2,r0_3,'o')
vlink(r0_3,r0_4,'o')
vlink(r0_4,r0_5,'o')
vlink(r0_5,r0_6,'o')
vlink(r0_6,r0_7,"o")
frame(o0,0,a)
frame(o1,1,a)
frame(o2,2,a)
frame(o3,3,a)
frame(o4,4,a)
frame(o5,5,a)
frame(o6,6,a)
frame(o7,7,a);
grid on
axis(600*[-1 1.5 -1 1 0 2])
view(145,20)
figuresk(1,20,1,14)